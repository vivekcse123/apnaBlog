app.use(
  express.static(browserDistFolder, {
    maxAge: '1y',
    index: false,
    redirect: false,
  }),
);

// A request for a path that looks like a static asset (has a static file
// extension) but wasn't served by express.static above doesn't exist -
// bots/scanners probing for .map/.env-lookalikes/etc. shouldn't trigger a
// full SSR render (which itself makes a backend API call) just to 404.
const STATIC_EXT_RE = /\.(?:js|css|map|json|txt|png|jpe?g|gif|svg|ico|webp|avif|woff2?|ttf|eot|xml)$/i;
app.use((req, res, next) => {
  if (STATIC_EXT_RE.test(req.path)) {
    res.status(404).type('text/plain').send('Not found');
    return;
  }
  next();
});

// Cache-Control overrides for SSR (RenderMode.Server) routes - Angular's own
// default header on these Function responses wins over vercel.json's static
// header rules, so the intended CDN caching for these routes never actually
// applies unless we set it here explicitly.
const SSR_CACHE_RULES: [string, string][] = [
  ['/blog/',   'public, s-maxage=120, stale-while-revalidate=86400'],
  ['/author/', 'public, s-maxage=300, stale-while-revalidate=86400'],
  ['/tag/',    'public, s-maxage=600, stale-while-revalidate=86400'],
];

/**
 * Handle all other requests by rendering the Angular application.
 */
app.use((req, res, next) => {
  // Rewrite loopback Host headers so Angular SSR's SSRF guard allows the
  // request. Production requests (Host: apnainsights.com) pass through as-is.
  if (isLoopback(req.headers['host'] ?? '')) {
    req.headers['host'] = PUBLIC_HOST;
  }

  angularApp
    .handle(req)
    .then((response) => {
      if (!response) {
        // RenderMode.Client route - Angular returns null, serve the SPA shell.
        // Unknown URLs (the `**` PageNotFound route) get a real 404 status.
        if (!isKnownClientRoute(req.path)) {
          return res.status(404).sendFile(join(browserDistFolder, 'index.html'));
        }
        return res.sendFile(join(browserDistFolder, 'index.html'));
      }

      const cacheRule = SSR_CACHE_RULES.find(([prefix]) => req.path.startsWith(prefix));
      if (cacheRule && response.status < 300) {
        response.headers.set('Cache-Control', cacheRule[1]);
      }

      // If Angular SSR failed (e.g. backend cold-start timeout), distinguish
      // between bot and browser traffic:
      //  • Bots/crawlers (Googlebot, AdSense) get a 503 + Retry-After so they
      //    retry later rather than caching an empty page as "low value content".
      //  • Real browsers get the client shell so Angular can bootstrap and
      //    fetch the data itself - user experience is preserved.
      if (response.status >= 500) {
        console.warn(`SSR returned ${response.status} for ${req.url}`);
        const ua = (req.headers['user-agent'] ?? '').toLowerCase();
        const isBot = /googlebot|adsbot|mediapartners|bingbot|yandex|duckduckbot|slurp|baiduspider|facebookexternalhit|twitterbot|rogerbot|linkedinbot|embedly|quora|pinterest|slackbot|vkshare|outbrain|facebot|ia_archiver/.test(ua);
        if (isBot) {
          res.setHeader('Retry-After', '60');
          res.status(503).send(
            '<!doctype html><html lang="en"><head><meta charset="utf-8"><title>ApnaInsights - Service Temporarily Unavailable</title></head>' +
            '<body><h1>503 Service Temporarily Unavailable</h1><p>ApnaInsights is starting up. Please try again in a moment.</p></body></html>'
          );
          return;
        }
        res.sendFile(join(browserDistFolder, 'index.html'));
        return;
      }

      return writeResponseToNodeResponse(response, res);
    })
    .catch(next);
});

/**
 * When Angular SSR throws (e.g. API timeout during render), apply the same
 * bot/browser split: bots get 503 so they retry; browsers get the SPA shell.
 */
app.use((err: unknown, req: Request, res: Response, _next: NextFunction) => {
  console.error('SSR render error:', err);
  const ua = (req.headers['user-agent'] ?? '').toLowerCase();
  const isBot = /googlebot|adsbot|mediapartners|bingbot|yandex|duckduckbot|slurp|baiduspider|facebookexternalhit|twitterbot|rogerbot|linkedinbot|embedly|quora|pinterest|slackbot|vkshare|outbrain|facebot|ia_archiver/.test(ua);
  if (isBot) {
    res.setHeader('Retry-After', '60');
    res.status(503).send(
      '<!doctype html><html lang="en"><head><meta charset="utf-8"><title>ApnaInsights - Service Temporarily Unavailable</title></head>' +
      '<body><h1>503 Service Temporarily Unavailable</h1><p>ApnaInsights is starting up. Please try again in a moment.</p></body></html>'
    );
    return;
  }
  res.sendFile(join(browserDistFolder, 'index.html'));
});

/**
 * Start the server if this module is the main entry point, or it is ran via PM2.
 * The server listens on the port defined by the `PORT` environment variable, or defaults to 4000.
 */
if (isMainModule(import.meta.url) || process.env['pm_id']) {
  const port = process.env['PORT'] || 4000;
  app.listen(port, (error) => {
    if (error) {
      throw error;
    }
  });
  // NOTE: Do NOT add a setInterval keepalive here.
  // On Vercel (serverless), this function process is killed after each request -
  // any interval set here never fires. Use an external uptime monitor (e.g.
  // UptimeRobot free tier) to ping the Render backend every 5 minutes instead.
}

